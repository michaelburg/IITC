// data type has two categories:
// 1. Primitive Data Types
// 2. Object / Reference Data Types

// PRIMARY DATA TYPES:

// String:
const firstName = "John";
const lastName = "Doe";

// Number:
const age = 30;
const degree = 36.7;

// Boolean:
const isMarried = false;
const isSingle = true;

// Undefined:
let car;
const bike = undefined;

// Null:
const house = null;

///////////////////////////////////////////////////////////////////

// OBJECT / REFERENCE DATA TYPES: (more on this later)

// Object:
const person = {
  firstName: "John",
  lastName: "Doe",
  age: 30,
  isMarried: false,
};

// Array:
const numbers = [1, 2, 3, 4, 5];
const fruits = ["Apple", "Banana", "Mango"];

// Function:
function sayHello() {
  console.log("Hello");
}
